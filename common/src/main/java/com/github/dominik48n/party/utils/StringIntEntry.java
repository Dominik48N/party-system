package com.github.dominik48n.party.utils;

public class StringIntEntry {
  String string;
  int anInt;
  
  public StringIntEntry(String string, int anInt) {
    this.string = string;
    this.anInt = anInt;
  }
  
  public String getString() {
    return string;
  }
  
  public int getInt() {
    return anInt;
  }
}
